void scroll_vert(sbyte delta_y)
{
  #if defined(__APPLE2__)
  byte rowcount;
  
  if (delta_y > 0)
    for (rowcount = 191; rowcount > 0; --rowcount)
    {
      memcpy(&HGR[RowsHGR[rowcount]], &HGR[RowsHGR[rowcount - 1]], COLS);
    }
  if (delta_y < 0)
    for (rowcount = 0; rowcount < 192; ++rowcount)
    {
      memcpy(&HGR[RowsHGR[rowcount - 1]], &HGR[RowsHGR[rowcount]], COLS);
    }
  #endif
  
  scroll_fine_y += delta_y;

  while (scroll_fine_y < 0) {
    scroll_fine_y += 8;
    scroll_up();
  }
  while (scroll_fine_y >= 8) {
    scroll_fine_y -= 8;
    scroll_down();    
  }
  
  
}

void scroll_horiz(sbyte delta_x) {
  scroll_fine_x += delta_x;
  #if defined(__APPLE2__)
  #endif
  while (scroll_fine_x < 0) {
    scroll_fine_x += 8;
    scroll_left();
  }
  while (scroll_fine_x >= 8) {
    scroll_fine_x -= 8;
    scroll_right();
  }
}

void Scroll(direction dir)
{
  byte count;
  #if __C64__
  ScrollingMaskOn();

  for (count = 0; count < 8; ++count)
  {
    wait_vblank(1);
    switch (dir)
    {
      case up:
        scroll_vert(-1);
        break;
      case down:
        scroll_vert(1);
        break;
      case left:
        scroll_horiz(-1);
        break;
      case right:
        scroll_horiz(1);
        break;
      default:
        break;
    }
    //wait_vblank(1);
    scroll_update_regs();
  }
  ScrollingMaskOff();
  #endif

  #if defined(__APPLE2__)
  for (count = 0; count < 8; ++count)
  {
    switch (dir)
    {
      case up:
        scroll_vert(-1);
        break;
      case down:
        scroll_vert(1);
        break;
      case left:
        scroll_horiz(-1);
        break;
      case right:
        scroll_horiz(1);
        break;
      default:
        break;
    }
  }
  #endif
}

//Buffer
void CopyBuffer()
{
  #if defined(__APPLE2__)
  byte x, y;
  int i = 0;
  for (y = 0; y < ROWS; ++y)
    for (x = 0; x < COLS; ++x)
    {
      DrawChar(ScreenChars[i],x, y);
      ++i;
    }
  #endif
  #if defined(__C64__)
  memcpy(&ScreenChars[0], &ScreenCharBuffer[0], 0x400);
  memcpy(&ScreenColors[0], &ScreenColorBuffer[0], 0x400);
  #endif
}

void CopyBufferArea(byte origin_x, byte origin_y, byte width, byte height)
{
  #if defined(__APPLE2__)
  byte x, y;
  int i = x + y * COLS;
  for (y = origin_y; y < origin_y + height; ++y)
  {
    i = y * COLS;
    for (x = origin_x; x < origin_x + width; ++x)
    {
      DrawChar(ScreenChars[i],x, y);
      ++i;
    }
  }
  #endif

  #if defined(__C64__)
  int offset = origin_x + YColumnIndex[origin_y];
  byte column;
  for (column = 0; column < height; ++column)
  {
    memcpy(&ScreenChars[offset], &ScreenCharBuffer[offset], width);
    memcpy(&ScreenColors[offset], &ScreenColorBuffer[offset], width);
    offset += COLS;
  }
  #endif
}

void PrintString(char text[20], byte posx, byte posy, bool fast, bool buffer)
{
  byte count;
  while (posy > ROWS)
    posy -= ROWS;
  for(count = 0; count < 20; ++count)
  {
    if (text[count] == '@')
      break;
    if (!fast)
      raster_wait(255);
    if (buffer)
      SetCharBuffer(text[count], posx + count, posy);
    else
      SetChar(text[count], posx + count, posy);      
  }
}

int originOffset = 0;
byte originX, originY = 0;
void SetTileOrigin(byte x, byte y)
{
  originOffset = YColumnIndex[y] + x;
  originX = x;
  originY = y;
}

byte indexes[4];

unsigned short offset1;
void DrawTileFast(byte index, byte x, byte y)
{
  index = (index << 1) + ((index >> 3) << 4);
  indexes[0] = index;
  indexes[1] = index + 1;
  indexes[2] = index + 16;
  indexes[3] = index + 17;

  x = x << 1;
  y = y << 1;

  #if defined(__C64__)
  offset1 = YColumnIndex[y] + x + originOffset;
  {
    memcpy(&ScreenChars[offset1], &indexes[0], 2);
    memcpy(&ScreenColors[offset1], &attributeset[indexes[0]], 2);
    offset1 += COLS;
    memcpy(&ScreenChars[offset1], &indexes[2], 2);
    memcpy(&ScreenColors[offset1], &attributeset[indexes[2]], 2);
  }
  #endif

  #if defined(__APPLE2__)
  SetChar(indexes[0], x + originX, y + originY);
  SetChar(indexes[1], x + originX + 1, y + originY);
  SetChar(indexes[2], x + originX, y + 1 + originY);
  SetChar(indexes[3], x + originX + 1, y + 1 + originY);
  #endif

}

void DrawLineH(char index, byte x, byte y, byte length)
{
  byte count = 0;
  for (count = 0; count < length; ++count)
    SetChar(index, x + count, y);
}
void DrawLineV(char index, byte x, byte y, byte length)
{
  byte count = 0;
  for (count = 0; count < length; ++count)
    SetChar(index, x, y + count);
}
void DrawBorder(char text[20], byte xPos, byte yPos, byte width, byte height, bool fill)
{
  byte x;
  DrawLineH(239, xPos + 1, yPos, width - 2);
  DrawLineH(239, xPos + 1, yPos + height - 1, width - 1);
  DrawLineV(255, xPos, yPos + 1, height - 1);
  DrawLineV(255, xPos + width - 1, yPos + 1, height - 1);
  SetChar(238, xPos, yPos);
  SetChar(238, xPos + width - 1, yPos);
  SetChar(238, xPos, yPos + height - 1);
  SetChar(238, xPos + width - 1, yPos + height - 1);
  PrintString(text, xPos + 1, yPos, true, false);
  if (fill)
    for (x = 0; x < height - 2; ++x)
    {
      DrawLineH(' ', xPos + 1, yPos + x + 1, width - 2);
    }
}

