void ClearScreen()
{
  
  #if defined(__C64__)
  memset(ScreenCharBuffer, ' ', 0x0400); // Clear Buffer
  memset(ScreenColors, 0, 0x0400); // clear Colors
  memset(ScreenColorBuffer, 0, 0x0400); // clear Color Buffer
  #endif
  
  #if defined(__APPLE2__)
  byte i;
  for (i = 0; i < 192; ++i)
    memset(&HGR[RowsHGR[i]], 0, 40); //Or just disable screen and clear it?
  //memset(HGR, 0, 0x2000); // clear HGR page 1
  #endif
  
  memset(ScreenChars, ' ', 0x0400); // Clear Chars (text page 1 on Apple II)
}

void raster_wait(byte line)
{
  #if defined(__C64__)
  while ((VIC.rasterline < line)){}
  #endif
  #if defined(__APPLE2__)
  byte x;
  while (++x < line){};
  #endif
}

void wait_vblank(byte frames) 
{
  byte count = frames;
  for (count = frames; count; --count)
  {
    raster_wait(255);    
  }
}

void InitializeGraphics()
{
  #if defined(__APPLE2__)
  byte y;
  ClearScreen();
  STROBE(0xc052); // turn off mixed-mode
  STROBE(0xc054); // page 1
  STROBE(0xc057); // hi-res
  STROBE(0xc050); // set graphics mode
  for (y = 0; y < 192; ++y)
    RowsHGR[y] = (y/64)*0x28 + (y%8)*0x400 + ((y/8)&7)*0x80;
  #endif
  
  #if defined(__C64__)
  #define bank 3
  byte screenpos = 2;
  byte charpos = 7;
  byte *CharRam = 0;
  #define CharacterRom 0xD000
  #define ColorRam 0xD800
  byte lastBank, lastScreenPos, lastCharPos;
  int screenposition;
  int* regd018 = (int*)0xd018;
  byte vicreg = 0x00;
  
  bgcolor(0);
  bordercolor(0);
  
  lastBank = bank;
  lastScreenPos = screenpos;
  lastCharPos = charpos;
  screenposition = (bank * (16*1024) + (screenpos * 1024));
  ScreenChars = 0;
  ScreenChars += screenposition;
  //CharRam += 2;
  CharRam += (bank * (16*1024) + charpos * 2048);
  
  memcpy(&CharRam[0], &charset[0], 256*8);
  
  ScreenCharBuffer = 0;
  ScreenCharBuffer += screenposition;
  if (bufferselect)
    ScreenCharBuffer -= 0x0400; // Buffer location 1024b after the screen position
  else
    ScreenCharBuffer += 0x0400; // Buffer location 1024b after the screen position
  ScreenColorBuffer = 0;
  ScreenColorBuffer += 0x0400; // Use the default screen character space for color buffer
   //Select Bank
  POKE (0xDD00, (PEEK(0XDD00)&(255 - bank)));
  //Set Screen and Character Ram Position
  screenpos = screenpos << 4;
  charpos = charpos << 1;
  vicreg = screenpos + charpos;
  //raster_wait(252);
  regd018[0] = vicreg;
  //Cursor Position
  POKE (0x0288, screenposition / 256);
  ClearScreen();
  #endif
}

#if defined(__APPLE2__)
void DrawChar(int index, byte xpos, byte ypos)
{
  byte y;
  int offset, i;
  i = index <<3;
  ypos = ypos << 3;
  offset= RowsHGR[ypos] + xpos;
  for (y = 0; y < 8; ++y)
  {
    HGR[offset] = charset[i];
    offset += 0x400;
    ++i;
  }
}
#endif

void SetChar(byte index, byte x, byte y)
{
  #if defined(__APPLE2__)
  ScreenChars[x + COLS * y] = index;
  DrawChar(index, x, y);
  #endif
  #if defined(__C64__)
  ScreenChars[x + COLS*y] = index;
  ScreenColors[x + COLS*y] = attributeset[index];
  #endif
}

void SetCharBuffer(byte index, byte x, byte y)
{
  #if defined(__APPLE2__)
  ScreenChars[x + COLS * y] = index;
  #endif
  #if defined(__C64__)
  ScreenCharBuffer[x + COLS*y] = index;
  ScreenColorBuffer[x + COLS*y] = attributeset[index];
  #endif
}

void CopyBuffer()
{
  #if defined(__APPLE2__)
  byte x, y;
  int i = 0;
  for (y = 0; y < ROWS; ++y)
    for (x = 0; x < COLS; ++x)
    {
      DrawChar(ScreenChars[i],x, y);
      ++i;
    }
  #endif
  #if defined(__C64__)
  memcpy(&ScreenChars[0], &ScreenCharBuffer[0], 1000);
  memcpy(&ScreenColors[0], &ScreenColorBuffer[0], 1000);
  #endif
}

void CopyBufferArea(byte origin_x, byte origin_y, byte width, byte height)
{
  #if defined(__APPLE2__)
  byte x, y;
  int i = x + y * COLS;
  for (y = origin_y; y < origin_y + height; ++y)
  {
    i = y * COLS;
    for (x = origin_x; x < origin_x + width; ++x)
    {
      DrawChar(ScreenChars[i],x, y);
      ++i;
    }
  }
  #endif
  
  #if defined(__C64__)
  int offset = origin_x + YColumnIndex[origin_y];
  byte column;
  for (column = 0; column < height; ++column)
    {
      	memcpy(&ScreenChars[offset], &ScreenCharBuffer[offset], width);
      	memcpy(&ScreenColors[offset], &ScreenColorBuffer[offset], width);
      	offset += COLS;
    }
  #endif
}

void PrintString(char text[20], byte posx, byte posy, bool fast, bool buffer)
{
  byte count;
  for(count = 0; count < 20; ++count)
  {
    if (text[count] == '@')
      break;
    if (!fast)
      raster_wait(255);
    if (buffer)
      SetCharBuffer(text[count], posx + count, posy);
    else
      SetChar(text[count], posx + count, posy);      
  }
}

int originOffset = 0;
void SetTileOrigin(byte x, byte y)
{
  originOffset = YColumnIndex[y] + x;  
}

byte indexes[4];

unsigned short offset1;
void DrawTileFast(byte index, byte x, byte y)
{
  index = (index << 1) + ((index >> 3) << 4);
  indexes[0] = index;
  indexes[1] = index + 1;
  indexes[2] = index + 16;
  indexes[3] = index + 17;
  
  x = x << 1;
  y = y << 1;
  #if defined(__C64__)
  offset1 = YColumnIndex[y] + x + originOffset;
  {
    memcpy(&ScreenChars[offset1], &indexes[0], 2);
    memcpy(&ScreenColors[offset1], &attributeset[indexes[0]], 2);
    offset1 += COLS;
    memcpy(&ScreenChars[offset1], &indexes[2], 2);
    memcpy(&ScreenColors[offset1], &attributeset[indexes[2]], 2);
  }
  #endif
  
  #if defined(__APPLE2__)
  DrawChar(indexes[0], x + originOffset, YColumnIndex[y]);
  DrawChar(indexes[1], x + originOffset + 1, YColumnIndex[y]);
  DrawChar(indexes[2], x + originOffset, YColumnIndex[y]);
  DrawChar(indexes[3], x + originOffset + 1, YColumnIndex[y + 1]);
  #endif
             
}

