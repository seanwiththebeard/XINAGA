static void MoveCharacter(byte index, byte dir)
{
  byte checkCollision = CheckCollision(index, dir);

  if(!checkCollision)
  {
    sbyte posX = characters.posX[index];
    sbyte posY = characters.posY[index];
    sbyte qPosX = characters.absPosX[index] / quadSizeDouble;
    sbyte qPosY = characters.absPosY[index] / quadSizeDouble;
    bool scrollQuads = false;
    
    switch (dir)
    {
      case up:
        --posY;
        --characters.absPosY[index];
        if (posY < 0)
          posY = mapHeight - 1;
        if (posY % 16 == 15)
        {
          --qPosY;
          if(qPosY < 0)
            qPosY = mapMatrixHeight - 1;
        }
        break;
      case down:
        ++posY;
        ++characters.absPosY[index];
        if (posY >= mapHeight)
          posY = 0;
        if (posY % 16 == 0)
        {
          ++qPosY;
          if(qPosY == mapMatrixHeight)
            qPosY = 0;
        }
        break;
      case left:
        --posX;
        --characters.absPosX[index];        
        if (posX < 0)
          posX = mapWidth - 1;
        if (posX % 16 == 15)
        {
          --qPosX;
          if(qPosX < 0)
            qPosX = mapMatrixWidth - 1;
        }
        break;
      case right:
        ++posX;
        ++characters.absPosX[index];        
        if (posX >= mapWidth)
          posX = 0;
        if (posX % 16 == 0)
        {
          ++qPosX;
          if(qPosX == mapMatrixWidth)
            qPosX = 0;
        }
        break;
      default:
        break;
    }
    characters.posX[index] = posX;
    characters.posY[index] = posY;

    if (index == followIndex)
    {
      byte edgeCheckX = characters.posX[index] % 16;
      byte edgeCheckY = characters.posY[index] % 16;
      
      switch (dir)
      {
        case 0:
          if (edgeCheckY == 6)
            scrollQuads = true;
          break;
        case 1:
          if (edgeCheckY == 10)
            scrollQuads = true;
          break;
        case 2:
          if (edgeCheckX == 6)
            scrollQuads = true;
          break;
        case 3:
          if (edgeCheckX == 10)
            scrollQuads = true;
          break;
      }
      if (scrollQuads)
      {
        byte quadA, quadB;
        byte indexA, indexB;
        byte posX = characters.posX[followIndex];
        byte posY = characters.posY[followIndex];
        bool charPosX = !(posX & 8);
        bool charPosY = !(posY & 8);
        byte relH = dir | (charPosX ? 0 : 4);
        byte relV = dir | (charPosY ? 0 : 4);
        
        byte compareQuad = ((posX >> 4) & 1) | (((posY >> 4) & 1) << 1) | ((dir > 1) << 2);
        
        byte qx = characters.absPosX[followIndex] / quadSizeDouble;
        byte qy = characters.absPosY[followIndex] / quadSizeDouble;
        indexA = mapQuads[((qx + quadRel[relH].hA) & 15) + (((qy + quadRel[relV].vA) & 15) << 4)];
        indexB = mapQuads[((qx + quadRel[relH].hB) & 15) + (((qy + quadRel[relV].vB) & 15) << 4)];
        
        quadA = CompareQuadValueA[compareQuad];
        quadB = CompareQuadValueB[compareQuad];
        
        if (quadBuffer[quadA] != indexA)
          LoadQuadrant(indexA, quadA);
        if (quadBuffer[quadB] != indexB)
          LoadQuadrant(indexB, quadB);
      }
      ++Sessions[0].STEPS;
      ++stepCount;
    }
    DrawEntireMap(false);
  }
}

screenName MapUpdate()
{
  exitScreen = false;
  stepCount = 0;
  
  characters.tile[followIndex] = getPartyMember(0)->CLASS;
  SetTileOrigin(viewportPosX, viewportPosY);

  if(Entering)
  {
    byte x = Doors.posX[EnteringDoor];
    byte y = Doors.posY[EnteringDoor];
    characters.absPosX[0]  = x;
    characters.absPosY[0]  = y;
    characters.posX[0]  = x % quadSizeDouble; //Where is our player in the buffered quad data?
    characters.posY[0]  = y % quadSizeDouble;
  }
  Entering = false;
  LoadMapQuads();
  ScreenFadeIn();
  DrawEntireMap(true);
  DrawCharStats();
  DrawLocalMiniMap(false);

  while (!exitScreen)
  {
    UpdateInput();
    //if (InputChanged())
    {
      direction Dir = 4;
      if (InputUp())
        Dir = up;
      if (InputDown())
        Dir = down;
      if (InputLeft())
        Dir = left;
      if (InputRight())
        Dir = right;
      if (Dir < 4)
      {
        MoveCharacter(followIndex, Dir);
        CheckDoor();
        TickMoonPhase();        
        continue;
      }
      if (InputFire())
        if (InputChanged())
        {
          byte action;
          ResetMenu(" ", 6, true);
          SetMenuItem(0, "Search");
          SetMenuItem(1, "Attack");
          SetMenuItem(2, "Party");
          SetMenuItem(3, "Map");
          SetMenuItem(4, "Exit");
          SetMenuItem(5, "...");

          action = GetMenuSelection();
          ClearMenu();
          switch (action)
          {
            case 0:
              break;
            case 1:
              exitScreen = true;
              ScreenFadeOut();
              nextScreen = Combat;
              break;
            case 2:
              break;
            case 3:
              MiniMapPosX = 2;
              MiniMapPosY = 2;
              MiniMapWidth = 16;
              MiniMapHeight = 16;
              DrawMiniMap(true);
              WaitForInput();DrawEntireMap(true);
              ScreenFadeOut();
              break;
            case 4:
              exitScreen = true;
              nextScreen = EditParty;
              ScreenFadeOut();
              break;
            default:
              break;
          }
        }
    }
  }
  ScreenFadeOut();
  return nextScreen;
}
