void PrintString(char *text, byte posx, byte posy, bool fast, bool buffer)
{
  byte i;
  while (posy > ROWS - 1)
    posy -= ROWS;
  for(i = 0; i < COLS; ++i)
  {
    if (text[i] == '@' ||  text[i] == '\n')
      break;
    if (!fast)
      wait_vblank(1);
    if (buffer)
      SetCharBuffer(text[i], posx + i, posy);
    else
      SetChar(text[i], posx + i, posy);
    SetColor(ColorText, posx + i, posy);
  }
  wait_vblank(1);
}

int originOffset = 0;
byte MapOriginX = 0;
byte MapOriginY = 0;

void SetTileOrigin(byte x, byte y)
{
  originOffset = YColumnIndex[y] + x;
  MapOriginX = x;
  MapOriginY = y;
}

byte DrawTileX = 0;
